package javaProjects;

public class Boolean_data_type {

	public static void main(String[] args) {
		// Boolean variable can holds only two conditional values: true or false.
		boolean t = true;
		boolean f = false;
		System.out.println("Print the boolean data in t = " + t);
		System.out.println("Print the boolean data in f = " + f);
 // another example of using boolean data:
		boolean condition = t;
		condition = t;
		if (condition = true);
			System.out.println("The value in the boolean variable 'condition' is = " + condition);
		
	}

}
