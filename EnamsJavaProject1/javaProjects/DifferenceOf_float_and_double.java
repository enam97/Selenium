package javaProjects;

public class DifferenceOf_float_and_double {

	public static void main(String[] args) {
		/* float is a variable that can holds a large size decimal number.
		if the assigned value to a float variable does not fall within that range,
		the compiler will show Error. Example as follows: */
		
		float f = 22222.5f;
		System.out.println("Print the value of integer variable f =" + f);
		
		
		/* double is a variable that can holds a very large size decimal number. If the
		  assigned value of a double variable does not fall within that range,
		  the compiler will show error. Example as follows: */
		
		double d = 22222444444.6d;
		System.out.println("Print the value of double variable d =" + d);
	}

}

